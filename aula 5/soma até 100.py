s = 0  # Variável para armazenar a soma dos números
i = 1  # Variável para iterar de 1 até 100

while i <= 100:
    s += i  # Adiciona o valor de i à soma
    print(f"A soma de todos os números até {i} é: {s}")  # Imprime a soma até o valor atual de i
    i += 1  # Incrementa o valor de i

print("Feito!")  # Imprime "Feito!" quando o loop terminar